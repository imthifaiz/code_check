function checkorderno(orderno){	
		var urlStr = "../salesreturn/CheckOrderno";
		$.ajax( {
			type : "GET",
			url : urlStr,
			async : true,
			data : {
				ORDERNO : orderno,
				},
				dataType : "json",
				success : function(data) {
					if (data.status == "100") {
						alert("Order Number Already Exists");
						document.getElementById("sorno").focus();
						document.getElementById("sorno").value="";
						return false;	
					} 
					else 
						return true;
				}
			});
		 return true;
}

